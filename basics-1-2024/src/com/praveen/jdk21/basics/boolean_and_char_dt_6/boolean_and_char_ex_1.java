package com.praveen.jdk21.basics.boolean_and_char_dt_6;

public class boolean_and_char_ex_1 {

    public static void main(String[] args) {

        // boolean
        boolean isBook = true;
        System.out.println("value of isBook: " + isBook);

        // char
        //   only one char can be saved here
        //   unicode can also be saved
        char c1 = 'Z';
        System.out.println("Value of c1: " + c1);

        //   unicode can also be saved in char
        char c2 = '\u0021';
        System.out.println("Value of c2: " + c2);

    }
}
